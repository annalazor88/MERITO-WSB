nazwa = 'Wroclawski piesek'

print(nazwa)
print(nazwa[0])   # pierwszy znam
print(nazwa[6])   # 7my znak
print(nazwa[-1])  # Ostatni znak
print(nazwa[-3])  # 3ci od końca znak
print(nazwa[ 2  :  6  ])  # od 3 do 6
print(nazwa[  -5 : -1])   # do ostatniego, bez ostatniego
print(nazwa[  : 7  ])  # od początku do znaku 7go
print(nazwa[  5  :   ])   # od 6go znaju do końca
print(nazwa[:])   # wszystko
print(nazwa[3:-2:2])  # co drugi znak